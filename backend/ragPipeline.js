const DEMO_DOCUMENT = `
Knowledge Copilot — Technical Reference

## What is RAG?
Retrieval-Augmented Generation (RAG) is a technique that combines information retrieval with language model generation. Instead of relying solely on the model's parametric knowledge, RAG fetches relevant documents at inference time and conditions the answer on that retrieved context. This grounds responses in real data and dramatically reduces hallucinations.

## Level 1: Query Rewriting
Raw user queries are often ambiguous or poorly phrased for retrieval. Query rewriting uses an LLM to expand, clarify, and reformulate the question into a retrieval-optimized form. For example, "what is HyDE?" becomes "Explain Hypothetical Document Embedding (HyDE) technique used in RAG pipelines for improving semantic search quality." This step alone can lift recall by 15–30%.

## Level 2: HyDE (Hypothetical Document Embedding)
HyDE (Hypothetical Document Embedding) is a technique where instead of embedding the query directly, the system first asks the LLM to generate a hypothetical "ideal answer" document, then embeds that hypothetical document for retrieval. The intuition is that the hypothetical answer lives in the same embedding space as real answers, leading to better nearest-neighbor matches. HyDE is especially effective for abstract or domain-specific questions where the query and relevant documents use different vocabulary.

## Level 3: Hybrid Retrieval
Hybrid retrieval combines two complementary signals: dense (semantic) retrieval using vector embeddings and sparse (lexical) retrieval using BM25 or TF-IDF. Dense retrieval excels at semantic similarity — it finds conceptually related content even when exact terms differ. Sparse retrieval excels at exact keyword matching — critical for names, codes, and technical terms. Combining both with Reciprocal Rank Fusion (RRF) produces significantly better recall than either alone. A typical hybrid system achieves 10–25% improvement in recall@10 over pure dense retrieval.

## Level 4: Reranking
After hybrid retrieval, the top-k candidate chunks (typically 20–50) are passed through a cross-encoder reranker. Unlike bi-encoders used for retrieval, cross-encoders jointly process the query and each candidate chunk together, producing a more accurate relevance score. The top 5–10 reranked chunks are then passed to generation. Reranking is computationally expensive but only runs on a small candidate set, making the latency overhead acceptable. Models like Cohere Rerank or a fine-tuned BERT achieve strong performance.

## Level 5: Grounded Generation
The final answer is generated by a large language model conditioned strictly on the retrieved and reranked context. The system prompt instructs the model to only use information from the provided context, cite sources using [1], [2] notation, and explicitly state when information is not available. This grounding step is what separates RAG from open-ended generation — the model cannot hallucinate facts that aren't in the retrieved context.

## Level 6: Faithfulness Verification
After generation, a faithfulness check asks the LLM to evaluate whether each claim in the generated answer is actually supported by the retrieved context. It assigns a confidence score from 0 to 100. Scores above 80 indicate high faithfulness. Scores below 50 flag potential hallucinations for human review. This self-evaluation loop allows the system to surface uncertainty rather than presenting low-confidence answers as fact.

## Chunking Strategy
Effective RAG depends heavily on how documents are split into chunks. Fixed-size chunking (e.g., 512 tokens with 50-token overlap) is simple but often splits semantic units mid-sentence. Semantic chunking uses sentence embeddings to detect topic boundaries and split there instead. Hierarchical chunking maintains parent-child relationships between document sections and paragraphs. The best strategy depends on document type: semantic chunking works best for prose, structural chunking for code and tables.

## Vector Stores
Popular vector databases include Pinecone (managed, production-grade), Weaviate (open-source, schema-aware), Qdrant (open-source, fast), and pgvector (PostgreSQL extension). For small datasets (<100K vectors), in-memory similarity search is sufficient. At scale, approximate nearest neighbor (ANN) algorithms like HNSW provide sub-millisecond retrieval with 95%+ recall.

## Evaluation Metrics
Key RAG metrics: Context Recall — what fraction of relevant information was retrieved? Context Precision — how much of the retrieved context was actually relevant? Answer Faithfulness — are all answer claims supported by context? Answer Relevance — how well does the answer address the original question? The RAGAS framework automates evaluation of all four metrics using an LLM judge.
`;

// ── Utility: cosine similarity ────────────────────────────────
function cosine(a, b) {
  let dot = 0, na = 0, nb = 0;
  for (let i = 0; i < a.length; i++) { dot += a[i] * b[i]; na += a[i] ** 2; nb += b[i] ** 2; }
  return dot / (Math.sqrt(na) * Math.sqrt(nb) + 1e-8);
}

// ── Utility: BM25 score ───────────────────────────────────────
function bm25Score(query, doc, avgLen, k1 = 1.5, b = 0.75) {
  const qTerms = query.toLowerCase().split(/\W+/).filter(Boolean);
  const dTerms = doc.toLowerCase().split(/\W+/).filter(Boolean);
  const dLen = dTerms.length;
  const freq = {};
  dTerms.forEach(t => (freq[t] = (freq[t] || 0) + 1));
  let score = 0;
  qTerms.forEach(t => {
    const tf = freq[t] || 0;
    const idf = Math.log((10 + 1) / (tf > 0 ? 1 : 0.5) + 1);
    score += idf * (tf * (k1 + 1)) / (tf + k1 * (1 - b + b * dLen / avgLen));
  });
  return score;
}

// ── Utility: simple embedding (character n-gram TF) ───────────
function simpleEmbed(text, dim = 64) {
  const t = text.toLowerCase();
  const vec = new Float32Array(dim);
  for (let i = 0; i < t.length - 1; i++) {
    const h = ((t.charCodeAt(i) * 31 + t.charCodeAt(i + 1)) * 17) % dim;
    vec[Math.abs(h)] += 1;
  }
  const norm = Math.sqrt(vec.reduce((s, v) => s + v * v, 0)) + 1e-8;
  return Array.from(vec).map(v => v / norm);
}

// ── Semantic chunking ─────────────────────────────────────────
function chunkDocument(text, targetSize = 900, overlap = 120) {
  const normalized = String(text || '')
    .replace(/\r\n/g, '\n')
    .replace(/\r/g, '\n')
    .trim();

  if (!normalized) return [];

  const blankParagraphs = normalized
    .split(/\n\s*\n+/)
    .map(p => p.trim())
    .filter(Boolean);

  let units = blankParagraphs;

  // Fallback for single-paragraph notes, code, and short files.
  if (units.length <= 1) {
    const lines = normalized
      .split(/\n+/)
      .map(line => line.trim())
      .filter(Boolean);

    if (lines.length > 1) {
      units = lines;
    } else {
      units = [normalized];
    }
  }

  const chunks = [];
  let buffer = '';

  const flush = () => {
    const chunk = buffer.trim();
    if (chunk) {
      chunks.push({
        id: chunks.length,
        text: chunk,
        source: `chunk_${chunks.length + 1}`,
      });
    }
    buffer = '';
  };

  for (const unit of units) {
    if (!unit) continue;
    const addition = buffer ? `\n\n${unit}` : unit;

    if ((buffer + addition).length <= targetSize) {
      buffer += addition;
      continue;
    }

    if (buffer) flush();

    // Very long unit: split into overlapping character windows.
    if (unit.length > targetSize) {
      let start = 0;
      while (start < unit.length) {
        const slice = unit.slice(start, start + targetSize).trim();
        if (slice) {
          chunks.push({
            id: chunks.length,
            text: slice,
            source: `chunk_${chunks.length + 1}`,
          });
        }
        start += Math.max(1, targetSize - overlap);
      }
    } else {
      buffer = unit;
    }
  }

  flush();
  return chunks;
}

// ── Document Store ────────────────────────────────────────────
export class DocumentStore {
  constructor() {
    this.chunks = [];
    this.documents = [];
    this.nextDocId = 1;
    this.avgLen = 0;
    this.addDocument('Demo: Knowledge Copilot Reference', DEMO_DOCUMENT);
  }

  recomputeAverageLength() {
    this.avgLen = this.chunks.length
      ? this.chunks.reduce((s, c) => s + c.text.split(/\W+/).length, 0) / this.chunks.length
      : 0;
  }

  getDocuments() {
    return this.documents.map(doc => ({
      id: doc.id,
      name: doc.name,
      chunks: doc.chunks,
    }));
  }

  addDocument(name, text) {
    const docId = this.nextDocId++;
    const newChunks = chunkDocument(text).map(c => ({
      ...c,
      id: `${docId}:${c.id}`,
      docId,
      source: name,
      embedding: simpleEmbed(c.text),
    }));

    this.documents.push({
      id: docId,
      name,
      chunks: newChunks.length,
    });

    this.chunks.push(...newChunks);
    this.recomputeAverageLength();

    return { docId, chunkCount: newChunks.length };
  }

  removeDocument(docId) {
    const numericId = Number(docId);
    this.documents = this.documents.filter(doc => doc.id !== numericId);
    this.chunks = this.chunks.filter(chunk => chunk.docId !== numericId);
    this.recomputeAverageLength();
  }

  search(queryEmb, queryText, topK = 8) {
    if (!this.chunks.length) return [];

    return this.chunks
      .map(c => ({
        ...c,
        denseScore: cosine(queryEmb, c.embedding),
        sparseScore: bm25Score(queryText, c.text, this.avgLen || 1),
      }))
      .map(c => {
        const sourceBoost = c.source === 'Demo: Knowledge Copilot Reference' ? 0 : 0.03;
        return { ...c, hybridScore: 0.6 * c.denseScore + 0.4 * (c.sparseScore / 10) + sourceBoost };
      })
      .sort((a, b) => b.hybridScore - a.hybridScore)
      .slice(0, topK);
  }
}

