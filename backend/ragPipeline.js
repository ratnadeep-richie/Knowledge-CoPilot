// Level 4: Reranking (LLM-based cross-encoder simulation)
async function level4_rerank(query, candidates, apiKey) {
  const list = candidates.slice(0, 6).map((c, i) => `[${i}] ${c.text.slice(0, 200)}...`).join('\n\n');
  const ranking = await callGemini(
    apiKey,
    [{
      role: 'user',
      content: `Query: "${query}"\n\nCandidates:\n${list}\n\nReturn a JSON array of indices sorted by relevance (most relevant first), e.g. [2,0,4,1,3,5]. Return ONLY the JSON array.`
    }],
    'You are a relevance judge. Rank the candidates by how well they answer the query. Return only a JSON array of indices.',
    128
  );
  try {
    const order = JSON.parse(ranking.match(/\[[\d,\s]+\]/)?.[0] || '[0,1,2,3,4,5]');
    return order.map(i => candidates[i]).filter(Boolean);
  } catch {
    return candidates;
  }
}

// Level 5: Grounded Generation (streaming)
async function level5_generate(originalQuery, rerankedChunks, apiKey, onChunk) {
  const context = rerankedChunks.map((c, i) => `[${i + 1}] Source: ${c.source}\n${c.text}`).join('\n\n---\n\n');

  const system = `You are a precise knowledge assistant. Answer questions using ONLY the provided context.
- Cite sources inline as [1], [2] etc. matching the context numbers
- If the answer isn't in the context, say "I don't have information about that in the current knowledge base."
- Be concise and factual. Never fabricate information.`;

  const prompt = `Context:\n${context}\n\nQuestion: ${originalQuery}`;

  return await callGeminiStream(apiKey, prompt, system, 1024, onChunk);
}

// Level 6: Faithfulness Check
async function level6_faithfulness(answer, chunks, apiKey) {
  const context = chunks.map((c, i) => `[${i + 1}] ${c.text.slice(0, 300)}`).join('\n\n');
  const result = await callGemini(
    apiKey,
    [{
      role: 'user',
      content: `Context:\n${context}\n\nAnswer to evaluate:\n${answer}\n\nScore faithfulness 0-100 (how well the answer is supported by context). Return JSON: {"score": <number>, "verdict": "<one sentence explanation>"}`
    }],
    'You are a faithfulness judge. Evaluate if the answer is grounded in the provided context. Return only valid JSON with no markdown formatting.',
    200
  );
  try {
    const clean = result.replace(/```json|```/g, '').trim();
    const json = JSON.parse(clean.match(/\{[\s\S]*\}/)?.[0] || '{}');
    return { score: json.score ?? 75, verdict: json.verdict ?? 'Evaluation complete.' };
  } catch {
    return { score: 75, verdict: 'Could not parse faithfulness score.' };
  }
}

// ── Document Summarizer ───────────────────────────────────────
export async function summarizeDocument(name, text, apiKey) {
  const preview = text.slice(0, 6000); // limit context
  return await callGemini(
    apiKey,
    [{
      role: 'user',
      content: `Summarize the following document titled "${name}".\n\nProvide:\n1. A 2-3 sentence overview\n2. 3-5 key topics or main points (as bullet points)\n3. Any important terms or concepts\n\nDocument:\n${preview}`,
    }],
    'You are an expert document analyst. Create clear, concise summaries that capture the essence of documents. Use plain language.',
    512
  );
}

// ── Answer Summarizer ─────────────────────────────────────────
export async function summarizeAnswer(answer, apiKey) {
  if (answer.length < 200) return null; // short answers don't need a summary
  return await callGemini(
    apiKey,
    [{
      role: 'user',
      content: `Summarize the following answer in 1-2 sentences (TL;DR style):\n\n${answer}`,
    }],
    'You are a summarization assistant. Return only the concise TL;DR summary, nothing else.',
    128
  );
}

// ── Main pipeline entry point ─────────────────────────────────
export async function runRAGPipeline(query, store, apiKey, options = {}, callbacks = {}) {
  const {
    useHyde = true,
    useReranking = true,
    useFaithfulness = true,
  } = options;

  const {
    onStageStart = () => {},
    onStageComplete = () => {},
    onStreamChunk = () => {},
  } = callbacks;

  const trace = {};

  // ── L1: Query Rewriting ──
  onStageStart(1, 'Query Rewriting');
  const rewrittenQuery = await level1_queryRewrite(query, apiKey);
  trace.queryRewrite = { original: query, rewritten: rewrittenQuery };
  onStageComplete(1, trace.queryRewrite);

  // ── L2: HyDE ──
  let retrievalQuery = rewrittenQuery;
  if (useHyde) {
    onStageStart(2, 'HyDE Generation');
    const hypothetical = await level2_hyde(rewrittenQuery, apiKey);
    trace.hyde = { hypothetical };
    retrievalQuery = hypothetical;
    onStageComplete(2, trace.hyde);
  } else {
    onStageComplete(2, { skipped: true });
  }

  // ── L3: Hybrid Retrieval ──
  onStageStart(3, 'Hybrid Retrieval');
  const searchText = [query, rewrittenQuery, retrievalQuery].filter(Boolean).join(' ');
  const queryEmb = simpleEmbed([query, retrievalQuery].filter(Boolean).join(' '));
  const candidates = store.search(queryEmb, searchText, 12);
  trace.retrieval = {
    count: candidates.length,
    topChunks: candidates.slice(0, 3).map(c => ({
      source: c.source,
      score: c.hybridScore.toFixed(3),
      preview: c.text.slice(0, 100) + '...',
    })),
  };
  onStageComplete(3, trace.retrieval);

  // ── L4: Reranking ──
  let finalChunks = candidates;
  if (useReranking) {
    onStageStart(4, 'Reranking');
    finalChunks = await level4_rerank(rewrittenQuery, candidates, apiKey);
    trace.reranking = {
      before: candidates.slice(0, 3).map(c => c.source),
      after: finalChunks.slice(0, 3).map(c => c.source),
    };
    onStageComplete(4, trace.reranking);
  } else {
    onStageComplete(4, { skipped: true });
  }

  // ── L5: Grounded Generation ──
  onStageStart(5, 'Grounded Generation');
  const topChunks = finalChunks.slice(0, 5);
  let answer = '';
  if (topChunks.length === 0) {
    answer = 'I do not have enough information in the current knowledge base to answer that.';
    onStreamChunk(answer);
  } else {
    answer = await level5_generate(query, topChunks, apiKey, (partial) => {
      onStreamChunk(partial);
    });
  }
  trace.generation = { chunkCount: topChunks.length, answerLength: answer.length };
  onStageComplete(5, trace.generation);

  // ── L6: Faithfulness ──
  let faithfulness = null;
  if (useFaithfulness) {
    onStageStart(6, 'Faithfulness Check');
    faithfulness = await level6_faithfulness(answer, topChunks, apiKey);
    trace.faithfulness = faithfulness;
    onStageComplete(6, trace.faithfulness);
  } else {
    onStageComplete(6, { skipped: true });
  }

  return {
    answer,
    sources: topChunks,
    faithfulness,
    trace,
  };
}
