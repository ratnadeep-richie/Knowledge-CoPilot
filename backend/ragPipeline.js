// Level 1: Query Rewriting
async function level1_queryRewrite(query, apiKey) {
  const rewritten = await callGemini(
    apiKey,
    [{ role: 'user', content: `Rewrite this search query to be more specific and retrieval-optimized. Return ONLY the rewritten query, no explanation:\n\n"${query}"` }],
    'You are a query optimization expert. Expand acronyms, add relevant technical terms, and make the query more specific for semantic search. Output only the rewritten query.',
    256
  );
  return rewritten.trim();
}

// Level 2: HyDE
async function level2_hyde(rewrittenQuery, apiKey) {
  const hypothetical = await callGemini(
    apiKey,
    [{ role: 'user', content: `Write a short 2-3 sentence factual passage that directly answers: "${rewrittenQuery}"\n\nWrite it as if it were an excerpt from a technical documentation page.` }],
    'You are a technical writer. Generate a plausible factual passage that would be a perfect answer to the query. Be specific and technical.',
    300
  );
  return hypothetical.trim();
}

